package com.example.event_management.dto;


public class FormularDTO {
    private String nume;
    private String email;
    private String telefon;
    private String companie;
    private String detaliiEveniment;
    private String locatieEveniment;

    // Getters and Setters
    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTelefon() {
        return telefon;
    }

    public void setTelefon(String telefon) {
        this.telefon = telefon;
    }

    public String getCompanie() {
        return companie;
    }

    public void setCompanie(String companie) {
        this.companie = companie;
    }

    public String getDetaliiEveniment() {
        return detaliiEveniment;
    }

    public void setDetaliiEveniment(String detaliiEveniment) {
        this.detaliiEveniment = detaliiEveniment;
    }

    public String getLocatieEveniment() {
        return locatieEveniment;
    }

    public void setLocatieEveniment(String locatieEveniment) {
        this.locatieEveniment = locatieEveniment;


    }
}
